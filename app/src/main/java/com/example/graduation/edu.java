package com.example.graduation;

public class edu {
    private String apply_start;
    private String edu_start;
    private String institution;
    private String name;
    private String time;



    public edu(){}



    public String getApply_start() {
        return apply_start;
    }

    public void setApply_start(String apply_start) {
        this.apply_start = apply_start;
    }



    public String getEdu_start() {
        return edu_start;
    }

    public void setEdu_start(String edu_start) {
        this.edu_start = edu_start;
    }


    public String getInstitution() {
        return institution;
    }

    public void setInstitution(String institution) {
        this.institution = institution;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

}
